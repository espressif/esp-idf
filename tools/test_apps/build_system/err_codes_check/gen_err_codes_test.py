# Generate a C test source file that validates all ESP-IDF error codes
# are correctly registered and resolvable via esp_err_to_name().
#

import csv
import os
import sys


def generate_test_source(csv_path: str, output_path: str) -> None:
    """Generate a C source file that tests all error codes."""
    entries = []
    with open(csv_path, encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            name = row['name']
            value = int(row['value'])
            entries.append((name, value))

    lines = []
    lines.append('/*')
    lines.append(' * Auto-generated by gen_err_codes_test.py — do not edit')
    lines.append(' */')
    lines.append('')
    lines.append('#include <stdio.h>')
    lines.append('#include <string.h>')
    lines.append('#include "esp_err.h"')
    lines.append('')
    lines.append('/* Forward declaration */')
    lines.append('int check_err_code(esp_err_t code, const char *expected_name);')
    lines.append('')
    lines.append('int check_err_code(esp_err_t code, const char *expected_name)')
    lines.append('{')
    lines.append('    const char *actual = esp_err_to_name(code);')
    lines.append('    if (strcmp(actual, expected_name) != 0) {')
    lines.append('        printf("FAIL: esp_err_to_name(0x%x) = \\"%s\\", expected \\"%s\\"\\n",')
    lines.append('               (unsigned)code, actual, expected_name);')
    lines.append('        return 1;')
    lines.append('    }')
    lines.append('    return 0;')
    lines.append('}')
    lines.append('')
    lines.append('int err_codes_check_all(void)')
    lines.append('{')
    lines.append('    int failures = 0;')

    for name, value in entries:
        # Use #ifdef guards as the error code may not be defined for all targets
        lines.append(f'#ifdef {name}')
        lines.append(f'    failures += check_err_code({name}, "{name}");')
        lines.append('#endif')

    lines.append('')
    lines.append('    if (failures == 0) {')
    lines.append(f'        printf("All {len(entries)} error code checks passed.\\n");')
    lines.append('    } else {')
    lines.append(f'        printf("%d of {len(entries)} error code checks failed.\\n", failures);')
    lines.append('    }')
    lines.append('    return failures;')
    lines.append('}')
    lines.append('')

    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))

    print(f'Generated {output_path} with {len(entries)} error code checks')


if __name__ == '__main__':
    if len(sys.argv) != 3:
        print(f'Usage: {sys.argv[0]} <input.csv> <output.c>', file=sys.stderr)
        sys.exit(1)

    generate_test_source(sys.argv[1], sys.argv[2])
